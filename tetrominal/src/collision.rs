use crate::tetromino::PieceType;

const KICKS_JLSTZ: [[(i16, i16); 5]; 8] = [
    // 0в†’R (0в†’1)
    [(0, 0), (-1, 0), (-1, -1), (0, 2), (-1, 2)],
    // Rв†’0 (1в†’0)
    [(0, 0), (1, 0), (1, 1), (0, -2), (1, -2)],
    // Rв†’2 (1в†’2)
    [(0, 0), (1, 0), (1, 1), (0, -2), (1, -2)],
    // 2в†’R (2в†’1)
    [(0, 0), (-1, 0), (-1, -1), (0, 2), (-1, 2)],
    // 2в†’L (2в†’3)
    [(0, 0), (1, 0), (1, -1), (0, 2), (1, 2)],
    // Lв†’2 (3в†’2)
    [(0, 0), (-1, 0), (-1, 1), (0, -2), (-1, -2)],
    // Lв†’0 (3в†’0)
    [(0, 0), (-1, 0), (-1, 1), (0, -2), (-1, -2)],
    // 0в†’L (0в†’3)
    [(0, 0), (1, 0), (1, -1), (0, 2), (1, 2)],
];

const KICKS_I: [[(i16, i16); 5]; 8] = [
    // 0в†’R (0в†’1)
    [(0, 0), (-2, 0), (1, 0), (-2, 1), (1, -2)],
    // Rв†’0 (1в†’0)
    [(0, 0), (2, 0), (-1, 0), (2, -1), (-1, 2)],
    // Rв†’2 (1в†’2)
    [(0, 0), (-1, 0), (2, 0), (-1, -2), (2, 1)],
    // 2в†’R (2в†’1)
    [(0, 0), (1, 0), (-2, 0), (1, 2), (-2, -1)],
    // 2в†’L (2в†’3)
    [(0, 0), (2, 0), (-1, 0), (2, 1), (-1, 2)],
    // Lв†’2 (3в†’2)
    [(0, 0), (-2, 0), (1, 0), (-2, -1), (1, -2)],
    // Lв†’0 (3в†’0)
    [(0, 0), (1, 0), (-2, 0), (1, -2), (-2, 1)],
    // 0в†’L (0в†’3)
    [(0, 0), (-1, 0), (2, 0), (-1, 2), (2, -1)],
];

fn kick_index(from: usize, to: usize) -> usize {
    match (from, to) {
        (0, 1) => 0,
        (1, 0) => 1,
        (1, 2) => 2,
        (2, 1) => 3,
        (2, 3) => 4,
        (3, 2) => 5,
        (3, 0) => 6,
        (0, 3) => 7,
        _ => panic!("Invalid rotation transition: {from} в†’ {to}"),
    }
}

pub fn try_rotate(
    check: impl Fn(i16, i16, usize) -> bool,
    piece: PieceType,
    x: i16,
    y: i16,
    from_rot: usize,
    to_rot: usize,
) -> Option<(i16, i16, usize)> {
    let kicks = match piece {
        PieceType::I => &KICKS_I,
        _ => &KICKS_JLSTZ,
    };

    let idx = kick_index(from_rot, to_rot);
    for &(dx, dy) in &kicks[idx] {
        let nx = x + dx;
        let ny = y + dy;
        if !check(nx, ny, to_rot) {
            return Some((nx, ny, to_rot));
        }
    }
    None
}
